package Chapter4_Conditionals.java;

public class chpt4_02_logical_Relational {
    public static void main(String[] args) {
        boolean a=true;
        boolean b= false;
        // AND Operators
        if(a && b){
            System.out.println("Y");
        }
        else{
            System.out.println("N");
        }


//        OR Operators
        if(a || b){
            System.out.println("Y");
        }
        else{
            System.out.println("N");
        }

    }
}
